<h4 class="m1">ISO-Norm	</h4>
<p class="strong">Aufgabenangemessenheit </p>
<p class="m2">„Ein Dialog ist aufgabenangemessen, wenn er den Benutzer unterstützt, seine Arbeitsaufgabe effektiv und effizient zu erledigen.“ <br>
Funktionalität und Dialog müssen auf den charakteristischen Eigenschaften der Arbeitsaufgabe basieren, anstatt auf der zur Aufgabenerledigung eingesetzten Technologie. <br>
Beispiel: Aufgabenangemessene Vorbelegung von Standardwerten in Eingabefeldern. </p>


<p class="strong">Selbstbeschreibungsfähigkeit </p>
<p class="m2">„Ein Dialog ist selbstbeschreibungsfähig, wenn jeder einzelne Dialogschritt durch Rückmeldung des Dialogsystems unmittelbar verständlich ist oder dem Benutzer auf Anfrage erklärt wird.“ <br> 
Ein Dialog ist in dem Maße selbstbeschreibungsfähig, in dem für den Benutzer zu jeder Zeit offensichtlich ist, in welchem Dialog, an welcher Stelle im Dialog er sich befindet, welche Handlungen unternommen werden können und wie diese ausgeführt werden können. <br>
Beispiel: Wann wird eine Eingabe erwartet, in welchem Format, und was sind die nächsten Schritte bzw. mit welcher Funktion können diese bewerkstelligt werden? </p>

<p class="strong"> Erwartungskonformität </p>
<p class="m2">„Ein Dialog ist erwartungskonform, wenn er konsistent ist und den Merkmalen des Benutzers entspricht, z.B. den Kenntnissen aus dem Arbeitsgebiet, der Ausbildung und der Erfahrung des Benutzers sowie den allgemein anerkannten Konventionen.“ 
Beispiel: Labels von Funktionen und Tastenbelegungen werden konsistent in allen Masken und Menüs verwendet. </p>

<p class="strong">Fehlertoleranz </p>
<p class="m2">„Ein Dialog ist fehlertolerant, wenn das beabsichtigte Arbeitsergebnis trotz erkennbar fehlerhafter Eingaben entweder mit keinem oder mit minimalem Korrekturaufwand durch den Benutzer erreicht werden kann.“ <br>
Fehlertoleranz kann mit den folgenden Mitteln erreicht werden: Fehlererkennung und -vermeidung, Fehlerkorrektur, Fehlermanagement. <br>
Beispiel: Ein Eingabefeld erkennt eine fehlerhafte Eingabe automatisch und teilt dies dem/der Benutzer*in mit. Trotzdem kann der/die Benutzer*in die Arbeit erst einmal fortsetzen. </p>

<p class="strong">Steuerbarkeit </p>
<p class="m2">„Ein Dialog ist steuerbar, wenn der Benutzer in der Lage ist, den Dialogablauf zu starten sowie seine Richtung und Geschwindigkeit zu beeinflussen, bis das Ziel erreicht ist.“ <br>
Websites und Applikationen sind für ihre Besucher*innen fremdes Terrain. Auf fremdem Terrain fühlt sich der Mensch leicht unsicher und ist in seinen Handlungen unkoordiniert und unter Umständen ziellos. Je eher ihm die Möglichkeiten aufgezeigt werden, die ihm zur Verfügung stehen, desto früher und zielsicherer wird er agieren. <br>
Beispiel: In jedem Eingabefeld gibt es eine Möglichkeit, die letzte Eingabe rückgängig zu machen. Benutzer*innen haben jederzeit die Möglichkeit, den Dialog zu verlassen und ihn zu einem späteren Zeitpunkt wieder aufzunehmen. </p>

<p class="strong">Individualisierbarkeit </p>
<p class="m2">„Ein Dialog ist individualisierbar, wenn das Dialogsystem Anpassungen an die Erfordernisse der Arbeitsaufgabe, individuelle Vorlieben des Benutzers und Benutzerfähigkeiten zulässt.“ <br>
Anmerkung: Obwohl es oft wünschenswert ist, Benutzer*innen Möglichkeiten zu individuellen Einstellungen zur Verfügung zu stellen, ist dies kein Ersatz für ergonomisch gestaltete Dialoge.  <br>
Beispiel: Menüs und Symbolleisten (Toolbars) können individuell angezeigt und eingerichtet werden. </p>

<p class="strong">Lernförderlichkeit </p>
<p class="m3">„Ein Dialog ist lernförderlich, wenn er den Benutzer beim Erlernen des Dialogsystems unterstützt und anleitet.“ <br>
Beispiel: Es wird ein durchgängiges Konzept bei der Strukturierung von Dialogen verwendet, z.B. unter der horizontalen Menüebene werden immer nur zwei weitere Ebenen benötigt. Bei Shortcuts wird immer der Anfangsbuchstabe plus Strg-Taste verwendet.</p>

