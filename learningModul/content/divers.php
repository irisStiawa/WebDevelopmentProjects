<h4 class="m1">Dateigrößen </h4>
<p class="strong">Referenz f. übliche Deklaration(Binäreinheitensystem “Präfixe-binär“): </p>
<ul class="m3">
    <li>210 = 1024 = Kibibyte (KiB)</li>
    <li>220 = Nebibyte (NiB)</li>
    <li>230 = Gibibyte (GiB)</li>
    <li>240 = Tebibyte (TiB)</li>
</ul>

<p class="strong">korrekte Deklaration (SI-System „Präfixe-dekadisch“):</p>
<table class="m3">
    <tbody>
        <tr>
