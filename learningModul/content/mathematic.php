<h4>Algorithmus</h4>
<p class="kursiv fS-08">visualgo.net</p>
<p class="fS-08 m1">= Folge von Handlungen die definiert sind mit Entscheidungen und in der Handlungen wiederholt werden (wiederhole bis oder solange)</p>
 
<p class="m1">In der Informatik sortieren wir nach einem Kriterium = Größe der Zahl</p>

<p class="m1">Möglichkeiten um in der Informatik zu sortieren (In-Place Sortierung):</p>

<table class="m3">
    <tbody>
        <tr>
            <td class="strong">Selection-Sort:</td>
            <td>kleinstes Element wird an erste Stelle gestellt. Im normal Fall ein instabiler Algorithmus (Elemente der gleichen Größe können Plätze tauschen)- bei zufälligerer Sortierung ist der Sort die bessere Variante</td>
        </tr>
        <tr>
            <td class="strong">Bubble-Sort:</td>
            <td>es werden immer zwei Elemente ihrer Größe nach betrachtet, wenn der zweite kleiner ist dann werden sie getauscht. Mit dem größerem Element werden die weiteren Elemente verglichen (stabiler Algorithmus)</td>
        </tr>
        <tr>
            <td class="strong">Insertion-Sort: </td>
            <td>Elemente werden durchgelaufen und geschaut wo passt es der Größe nach hinein - stabiler Algorithmus (Elemente der gleichen Größe tauschen nicht die Plätze)- wenn nicht mehr viel zu sortieren ist funktioniert der Sort ziemlich gut</td>
        </tr>
        <tr>
            <td class="strong">Bogosort: </td>
            <td>Elemente werden solange gemischt bis sie in der richtigen Reihenfolge sind, schlechtester Algorithmus – (Durchschnittlich eine sehr hohe Laufzeit, hohe Wahrscheinlichkeit auf eine endliche Laufzeit, kleine mögl. auf unendliche Laufzeit)</td>
        </tr>
        <tr>
            <td class="strong">Quicksort:</td>
            <td>0(n*logn), eine Zahl wird als Angelpunkt verwendet, alle Zahlen werden verglichen ob größer oder kleiner als Angelpunkt, wenn kleiner dann werden diese getauscht, am Ende des Durchlaufes wird das mit dem Angelpunkt gewanderte Element getauscht, danach wird ein neuer Angelpunkt gewählt, nicht stabil</td>
        </tr>
        <tr>
            <td class="strong">Radix:</td>
            <td>teilt die Elemente nach der Größe der letzten Stelle auf und wandert dann immer um eine Stelle nach oben (instabiler Algorithmus)</td>
        </tr>

    </tbody>
</table>
	
