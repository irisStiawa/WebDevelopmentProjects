<h4 class="m1">Wrapper Klassen</h4>
<ul class="m2">
    <li>Sind vordefinierte Klassen aus der Java API
        <ul>
            <li>im Java Sprach-Standard enthalten (kein import notwendig)</li>
        </ul>
    </li>
    <li>Möglichkeiten einfache Datentypen wie Objekte zu verwenden
        <ul>
            <li>Polymorphie nützen</li>
            <li>Methoden darauf anwenden</li>
            <li>static Utility-Methoden</li>
            <li>in Objekten [] speichern und an andere Methoden übergeben</li>
        </ul>
    </li>
    <li><span class="strong">Packen</span> – Boxen
        <ul>
            <li>primitiver Datentyp -> Referenz Datentyp</li>
        </ul>
    </li>
    <li><span class="strong">Entpacken</span>-Unboxing
        <ul>
            <li>Referenz Datentyp -> primitiver Datentyp</li>
        </ul>
    </li>
    <li>static Methoden (static Method: kein Objekt der Klasse notwendig)
        <ul>
            <li>zum Parsen/Interpretieren</li>
            <li>Konvertieren</li>
        </ul>
    </li>
</ul>

