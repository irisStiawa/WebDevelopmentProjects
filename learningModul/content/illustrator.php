<h4>Konturen und Füllung</h4>
<p class="m1">= links unten in der Werkzeugleiste, die kleinen Symbole darunter definieren die Möglichkeiten: mit, Verlauf oder ohne</p>

<h4>Formwerkzeuge</h4>
<p class="m1">= bei linke Maustaste und ziehen wird eine Form aufgezogen, bei Klicken auf den Bildschirm wird ein Popup-Fenster geöffnet das die Werteingabe erlaubt</p>


<h4>Polygon</h4>
<p class="m1">= ein Vieleck</p>

<h4>Bildgrößen einfügen, ändern</h4>
<p class="m1">= Datei>Platzieren und eine Verknüpfung erstellen, Größe ändern immer über die Steuerungspalette um präzise Größen zu erhalten</p>

<h4>Schnittmaske erstellen</h4>
<p class="m1">= über das Bild einen Rahmen ziehen und mit rechts Klick>Schnittmaske erstellen, mit der Direktauswahl-Mauswerkzeug kann das Bild noch verzogen werden</p>

<h4>Hilfslinien erstellen</h4>
<p class="m1">= wie in Gimp aus dem Lineal ziehen, rechte Maustaste Hilfslinien fixieren abhacken um die Linien zu bewegen und zu löschen – Gefahr dabei ist das diese sich verschieben (<span class="strong">Tipp:</span> Hilfslinien auf eigene Ebene und Ebene sperren, bei Bedarf auch ausblenden), </p>

<p class="m1">Alttaste + Feiltaste = Hilfslinie wird kopiert</p>

<h4>Zwei Formen verbinden</h4>
<p class="m1">Pathfinder>Formmod>dem Formbereich hinzufügen</p>

<h4 class="m1">SVG Dateien</h4>
<ul class="m1">
    <li>markieren, rechts Klick, in Pfade umwandeln</li>
    <li>im Illustrator speichern als .svg-Datei</li>
</ul>

