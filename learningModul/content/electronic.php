<p class="strong m1">Zwei Arten des Stromflusses im Stromkreis</p>
<ul class="m3">
    <li>AC/DC</li>
    <li>Steht indirekt für die australischen Hard-Rock-Band AC/DC</li>
    <li>AC ist eine Abkürzung für Alternating Current. DC steht für Direct Current</li>
    <li>Der größte Unterscheid zwischen AC- und DC-Strom ist die Fließrichtung: Bei Gleichstrom(DC-Strom) fließt die elektrische Ladung nur in eine Richtung. Im Wechselstrom (AC-Strom) ändert sich periodisch und in stetiger Wiederholung ihre Richtung</li>
    <li>Die bekannteste Wechselstromquelle ist die Steckdose. Gleichstrom wird hauptsächlich in der Schwachstromtechnik eingesetzt, zum Beispiel in Batterien und Handy-Akkus.</li>
</ul>

<p class="border-black text-center m3"><span class="fC-red">Achtung! Es existiert ein Unterschied zwischen technischer und physikalischer Stromflussrichtung!</span><br><br><span class="strong">Technische Stromrichtung:</span> Strom fließt vom Pluspol zum Minuspol <br>
<span class="strong">Physikalische Stromrichtung:</span> Strom fließt vom Minuspol zum Pluspol</p>	

