<h4 class="m1">Cloud oder Lokaler Server</h4>

<p class="strong m1">Cloud (Remotelösung)</p>
<ul class="m3">
    <li>Ressource die benötigt ist nicht vor Ort</li>
    <li>Cloud Storage (Speicher auf einem Server außerhalb des eigenen Netzwerks)
        <ul>
            <li>Sharepoint oder One Drive for Business – für Unternehmen(Darstellung des internen Netzwerk in der Cloud)</li>
            <li>One Drive – für Private (angreifbarer Speicherplatz als bei Sharepoint)</li>
        </ul>
    </li>
    <li>Cloud Computing (Rechenleistung auf einem Server außerhalb des eigenen Netzwerks, z.B. Googlerechner)</li>
</ul>

<h4></h4>

<p class="strong m1">Lokale Server</p>
<ul class="m3">
    <li>Ressource die benötigt ist vor Ort</li>
    <li>ThinClient (minimalleistungsfähige Computer, gesamte Leistung kommt vom Server)</li>
</ul>

