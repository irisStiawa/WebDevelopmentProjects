<h4 class="m1">Klassifikation – Die Genrationen</h4>

<p class="strong">First Generation Language (1GL)</p>
<ul class="m2">
    <li>Auf Maschinenebene</li>
    <li>Anweisungen von Binär</li>
    <li>Anpassung an bestimmten Computer/CPU</li>
</ul>

<p class="strong">Second Generation Language (2GL)</p>
<ul class="m1">
    <li>Assembler – Sprachen</li>
    <li>„lesbar“ (+/-)</li>
    <li>enthält einzelne Anweisungen an den Prozessor</li>
    <li>Maschinenabhängig</li>
</ul>
	
<p class="kursiv m2">10110000 01100001		Assembler Code: movb $0x61, %al</p>	

<p class="strong">Third Generation Language (3GL)</p>
<ul class="m2">
    <li>High Level Programmiersprachen</li>
    <li>Höherer Abstraktionsgrad</li>
    <li>Leichter lesbar und wartbar</li>
    <li>unabhängiger von bestimmten Maschinen</li>
    <li>müssen kompiliert (am Ende des Entwicklungsprozesses) oder interpretiert (während der Programmlaufzeit) werden</li>
</ul>

<p class="strong">Fourth Generation Language (4GL)</p>	
<ul class="m3">
    <li>Begriff nicht klar definiert</li>
    <li>wenig Code, um bestimmte Funktionalitäten zu erreichen</li>
    <li>z.B Formulargeneratoren</li>
</ul>

