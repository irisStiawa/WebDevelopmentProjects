    <h4 class ="m2 fS-140 text-center">Geschichte des Webbrowsers</h4>

    <p class ="m1">Webbrowser sind Programme die HTML-Codes abrufen. Es gibt kein Gesetz das HTML-Codes nach einer Vorlage interpretiert werden müssen, demnach gab es zu den Anfängen der Webbrowser unterschiedliche Ergebnisse für die selben Quellcodes. </p>

    <p class ="m1">Die NCSA Mosaic ist ein 1993 am National Center for Supercomputing Applications (NCSA) entwickelter Webbrowser. Microsoft erwarb 1994 für 2 Millionen Dollar eine Generallizenz. Bis zur Version 6 trug der Microsoft Internet Explorer den Hinweis „Basiert auf NCSA Mosaic“. Mit dem Beginn der wachsenden Popularität des World Wide Web wurden Webbrowser schnell bekannt. Der Name Mosaic wurde in den Jahren 1993/1994 kurzzeitig zum Synonym für Webbrowser. 1994 stieg Marc Andreessen aus dem NCSA-Team aus und gründete mit anderen das Unternehmen Netscape, das den Browser Netscape Navigator entwickelte, der den NCSA Mosaic rasch als führenden Browser ablöste. Netscape hatte den Hauptmarktanteil mit dem Navigator da dieser Java unterstützte und auf Microsoftrechnern lief. Nach Entwicklung des Internet Explorers verlor Netscape im sogenannten Browserkrieg zusehends Marktanteile, da dieser mit dem Betriebssystem Microsoft Windows mitgeliefert wurde. (Microsoft behauptete das der Internet Explorer sich vom Betriebssystem nicht trennen lies.)</p>

    <p class ="m1">Microsoft setzte in jener Zeit zudem auf eine selbstentwickelte Laufzeitumgebung für Java-Anwendungen, die mit Suns Java-Plugin nicht voll kompatibel war, <span class="strong"> vom Netscape Navigator aber schon. </span> Netscape hatte daher mit Microsofts Java ebenfalls Darstellungsschwierigkeiten. Analog ging Microsoft mit JavaScript vor, das Microsoft als JScript mit eigenem Code erweiterte, den Netscape im Gegensatz zum Internet Explorer nicht interpretieren konnte. Netscape konnte daher für Internet Explorer optimierte Websites oftmals nur unzulänglich bis praktisch überhaupt nicht darstellen.</p>

    <p  class ="m1">Die HTML-Quellcodes wurden von beiden Webbrowsern unterschiedlich interpretiert und die Programmierer hatten Schwierigkeiten die Quellcodes so hinzubiegen das sie auf allen Webbrowsern ähnlich fungierten.</p>

    <p  class ="m1">Als die Einstellung von Netscape bevorstand, stellte Netscape 1998 den Programmcode seines Browsers unter eine Open-Source-Lizenz und gründete zur Koordination der Weiterentwicklung die Mozilla Foundation.</p>


    <p  class ="m3">2008 entwickelte Google Chrome. Chrome ist speziell von Google entworfen worden sehr schnell und gut Javascript aufzurufen. </p>


