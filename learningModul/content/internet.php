<h4 class="m1">Klassifikation – Compiler vs. Interpreter</h4>

<p class="strong">Compiler-Sprachen</p>
<ul class="m2">
    <li>Quellcode wird einmalig in Maschinencode übersetzt (kompiliert)</li>
    <li>Effizient, optimierter Code</li>
    <li>Höherer Aufwand im Development, jede Codeänderung muss erneut kompliert werden</li>
</ul>

<p class="strong">Interpreter-Sprachen</p>	
<ul class="m3">
    <li>Quellcode wird zur Laufzeit auf Maschinencode übersetzt</li>
    <li>Sofortiges Testen in der Entwicklung</li>
    <li>Langsamer, ineffizient</li>
</ul>

