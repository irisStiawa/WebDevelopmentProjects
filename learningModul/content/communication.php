<ul class="m1">
    <li>Komplexität eines Backlogs Items wird in Story Points ausgedrückt</li>
    <li>Story Points werden mit Zeit- oder Geldfaktor verknüpft</li>
    <li>Erfahrung ist notwendig, um korrekt schätzen zu können</li>
</ul>

