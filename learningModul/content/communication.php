<p class="strong m1">Yerks-Dodson-Gesetz</p>

<p class="m1">Bei einer Unterforderung wird das Niveau der Leistungsfähigkeit nicht erreicht.</p>

<p class="m1">Die Steigerung des Aktivationsniveau führt zu einer Erhöhung der Leistung bis zu einem Maximalwert.</p>

<p class="m1">Erhöht sich das Aktionsniveau weiterhin, nimmt die Leistung ab.</p>

<p class="m3"><span class="strong">Fazit: </span>Sind Menschen mittelmäßig aktiv führt das zum Leistungsoptimum.</p>

<p class="strong m1">Symptome der Prüfungsangst</p>
<ul class="m3">
    <li>Denkblockaden</li>
    <li>Die Hände zittern oder schwitzen</li>
    <li>Die Atmung wird flach</li>
