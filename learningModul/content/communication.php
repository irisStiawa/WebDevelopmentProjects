<p class="strong">Was ist Scrum?</p>
<ul class="m3">
    <li>Vorgehensweise für Entwicklungsprojekte</li>
    <li>Fokus auf Flexibilität und Dynamik</li>
    <li>Anwendung in Produkt- und Projektmanagement</li>
    <li>Ursprung in der Softwareentwicklung</li>
</ul>
 
<p class="strong">Rollen</p>
<ul class="m3">
    <li>Product Owner</li>
    <li>Scrum Master</li>
    <li>Team Member</li>
</ul>

<p class="strong">Product Owner</p>
<ul class="m3">
    <li>Vertritt die Anwenderinnen des Produkts</li>
    <li>Vertritt die Stakeholder des Projekts</li>
    <li>Muss wissen, was die Kunden wollen</li>
</ul>

<p class="strong">Scrum Master</p>
<ul class="m3">
    <li>Trägt Verantwortung für den Scrum Prozess</li>
    <li>Moderator und Unterstützer des Projektteams</li>
    <li>Beseitigt Hindernisse, fördert Zusammenarbeit</li>
    <li>Ansprechpartner für Projektumfeld</li>
</ul>

<p class="strong">Project Team</p>	
<ul class="m3">
    <li>5-10 Members</li>
    <li>Organisieren ihre Aufgaben selbst</li>
    <li>Keine Hierarchie</li>
    <li>Die selben Rechte und Pflichten, unterschiedliche Kompetenzen <br><br>
    <span class="kursiv">Idealfall</span><br>
    <ul>
        <li>Alle Members aus einem Antrieb dabei</li>
        <li>Members suchen sich ihr Projekt selber aus</li>
    </ul></li>
</ul>

<p class="strong">Wichtige Begriffe</p>
<ul class="m3">
    <li>Epics</li>
    <li>User Stories (beschreibt Anforderung aus Usersicht)</li>
    <li>Story Card</li>
    <li>Product Backlog</li>
    <li>Definition of Done(DoD)</li>
    <li>Story Points</li>
    <li>Sprint</li>
    <li>Backlog</li>
    <li>Daily Scrum</li>
    <li>Sprint Review</li>
    <li>Sprint Rerospective</li>
</ul>

<p class="strong">Epics, User Stories und Story Cards</p>
<ul class="m2">
    <li>User Storys sind Software Anforderungen</li>
    <li>In Alltagssprache formuliert</li>
    <li>Kurz gehalten, 1-2 Sätze</li>
    <li>Zusammenhängende User Stories werden in eine Epic erfasst</li>
    <li>Jede User Story wird auf eine Story Card geschrieben</li>
