<p>Wann ist ein Backlog Item fertig?</p>
<ul class="m3">
    <li>Welche Indikatoren sind erkennbar?</li>
    <li>Welche Funktionen müssen vorhanden sein?</li>
    <li>Welche Tests müssen absolviert worden sein?</li>
</ul>

