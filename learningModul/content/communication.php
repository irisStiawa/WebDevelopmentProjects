            <li>subjektive Wahrnehmung</li>
        </ul>
    </div>
    <div class="ml2">
        <p class="strong">Dis-Stress</p>
        <ul>
            <li>negativer Stress</li>
            <li>Anforderungen die überfordern</li>
            <li>ängstlich, gereizt, erschöpft</li>
        </ul>
    </div>

</div>
	

<p class="strong">Auslöser von Stress</p>
<ul class="m2">
    <li>der Auslöser von Stress werden Stressoren genannt</li>
