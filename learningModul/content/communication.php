<h4 class="m1">Wie kann man klassisches Projektmanagement von agilem Projektmanagement unterscheiden?</h4>

<p class="strong">Merkmale klassisches Projektmanagement:</p>
<ul class="m3">
    <li>die Projektanforderungen sind zu beginn genau bekannt</li>
    <li>für spätere Änderungen entstehen hohe Kosten</li>
    <li>die Auftraggeber bekommen erst das Endergebnis zu sehen</li>
    <li>im Projektteam herrschen klare Hierarchien</li>
    <li>lange Meetings und klassische Dokumentationen werden durchgeführt</li>
    <li>die Aufwandsschätzung übernimmt der Projektleiter</li>
</ul>

<p class="strong">Klassisches Projektmanagement</p>
<p class="m3">Im klassischen Projektmanagement werden Projekte vorab genau geplant, eine Phase folgt auf die nächste. Diese Form Projekte umzusetzen wird auch Wasserfall genannt und ist ein Klassiker unter dem Projektmanagement-Methoden. Wasserfall deshalb, weil ein Wasserfall nur von oben nach unten verlaufen kann. Hierarchisch besetzte Unternehmen setzen Projekte häufig mit dieser Methode um. Bei der Methode werden große Projekte in mehrere Phasen unterteilt die aufeinander Aufbauen und in einer vorher festgelegten Reihe durchgeführt werden. Wenn eine Phase abgeschlossen ist sind nachträgliche Änderungen schwierig, kostenaufwändig und auch nicht erwünscht. Eignet sich gut für Projekte die konstante Anforderungen haben, wie Bauprojekte. Ein einfaches Einfamilienhaus zu bauen ist mit Wasserfall optimal lösbar. </p>

<p class="strong">Wasserfall Vorteile </p>
<ul class="m3">
    <li>hohe Planungssicherheit</li>
    <li>umfangreiche Projekte können durch diese geordnete Struktur zuverlässig geplant werden.</li>
</ul>

<p class="strong">Wasserfall Nachteile</p>
<ul class="m3">
    <li>wenig flexibel</li>
    <li>Fehler in der Umsetzung werden oft erst am Ende des Projekts sichtbar</li>
    <li>eine Fehlerkorrektur ist teuer</li>
</ul>

<p class="strong">Merkmale agiles Projektmanagement:</p>	
<ul class="m3">
    <li>die Projektanforderungen sind zu Beginn nicht genau definiert, unklar</li>
    <li>wenn es zu Änderungen kommt entstehen nur geringe Kosten</li>
    <li>da die Auftraggeber bewerten immer auch Zwischenergebnisse und Feedback geben</li>
    <li>Teams sind selbst organisiert</li>
    <li>die Information erfolgt informell über Standup-Meetings</li>
    <li>die Aufwandschätzung passiert gemeinsam im Team</li>
</ul>

<p class="strong">Agiles Projektmanagement</p>
<p class="m3">Das agile Projektmanagement hat seinen Ursprung innerhalb der Softwareentwicklung, eine Gruppe von Entwicklern war mit dem Arbeiten nach dem Wasserfall-Prinzip unzufrieden. Das Ziel war eine neue Arbeitsweise zu finden die für alle beteiligten besser funktioniert. So das die Abstimmung mit Kunden öfter erfolgt und das Kunden Feedback regelmäßig eingeholt und umgesetzt wird. Diese neue Art Projekte umzusetzen wurde im agilen Manifest zusammengefasst. In Abgewandelter Form werden agile Arbeitsweisen auch für andere Projekte also nicht nur für Softwareentwicklungen eingesetzt. Das agile Manifest besteht aus vier Leitsätzen und zwölf Prinzipien. 
</p>

<p class="strong">Die vier Leitsätze:</p>
<ul class="m3">
    <li>Individuen und Interaktionen sind wichtiger als Prozesse und Werkzeuge</li>
    <li>funktionierende Software ist wichtiger als umfassende Dokumentation</li>
    <li>Zusammenarbeit mit dem Kunden ist wichtiger als Vertragsverhandlungen</li>
    <li>reagieren auf Veränderungen ist wichtiger als das befolgen eines Plans</li>
</ul>

<p class="strong">zwölf Prinzipien</p>
<ul class="m3">
    <li>Kunden Zufriedenheit eine der höchsten Prioritäten</li>
    <li>das Ziel ist eine wertvolle Software auszuliefern</li>
    <li>das die Anforderungsänderungen die Kunden einbringen auch noch später im Prozess willkommen sind um für den Kunden einen Wettbewerbsvorteil zu schaffen</li>
    <li>das Businesspeople und Entwickler bei einem Projekt wirklich zusammen arbeiten sollen</li>
    <li>effizienteste Methode um Informationen innerhalb der Gruppe weiter zu geben das Gespräche von Angesicht zu Angesicht</li>
    <li>Zusätzlich wird bei dem Manifest erwähnt das Projekte und motivierte Einzelpersonen herum aufgebaut werden sollen, das die ein Umfeld bekommen das sie unterstützt und das ihnen Vertrauen entgegengebracht wird das sie ihren Job gut machen.</li>
</ul>

<p class="strong">12 Prinzipien(Internetrecherche)</p>
<ul class="m3">
    <li>Kunden durch frühe und kontinuierliche Auslieferung
zufriedenstellen</li>
    <li>Veränderte Anforderungen auch spät im Projekt willkommen heißen</li>
    <li>Häufig Wert schöpfen</li>
    <li>Silos in Ihrem Projekt abreiße</li>
    <li>Projekte auf motivierten Individuen aufbauen</li>
    <li>der effektivste Kommunikationsweg ist von Angesicht zu Angesicht</li>
    <li>funktionierende Software ist das wichtigste Fortschrittsmaß</li>
    <li>eine nachthaltiges Arbeitstempo halten</li>
    <li>stetige Exzellenz verstärkt Agilität</li>
    <li>Einfachheit ist essenziell</li>
    <li>selbst organisierte Team erzeugen den meisten Wert</li>
    <li>regelmäßig reflektieren und die Arbeitsweise anpassen, um effektiver zu werden</li>
</ul>
 
