<h4 class="m1">Führungsstil</h4>
<p class="strong m1">Die unterschiedlichen Führungsstile können in 3 Modelle geteilt werden:</p>
<ul class="m2">
    <li>Der autoritäre Führungsstil</li>
    <li>Der Kooperative Führungsstil</li>
    <li>der Laissez-faire Führungsstil</li>
</ul>

<p class="strong m1">Der autoritäre Führungsstil</p>

<p class="kursiv">Merkmale:</p>
<p class="m1">Fokussiert auf Ergebnisse, Fakten und Leistung. <br> Die Führung basiert auf Anordnungen der Geschäftsführung.</p>

<p class="m3">Dieser Führungsstil wird auch als hierarchischer Führungsstil bezeichnet.</p>

<p class="strong m1">Der kooperative Führungsstil</p>

<p class="kursiv">Merkmale:</p>
<p class="m3">Führungskraft und Mitarbeiter*innen arbeiten zusammen und ergänzen sich in ihren Kompetenzen. Verantwortlichkeiten und Aufgaben werden besprochen und aufgeteilt.</p>

<p class="strong m1">Der  Laissez-faire Führungsstil</p>
<p class="kursiv">Merkmale:</p>
<p class="m3">Mitarbeiter*innen haben viele Freiheiten und Entscheidungsmöglichkeiten</p>


