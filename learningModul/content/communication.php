<h4>PRÄSENTATION: MEETING GUIDELINES</h4>
<p class="fS-08 m1">(Meeting is a conversation at least two people.)</p>

<p class="strong">ROLES</p>
<ul class="m2">
    <li>Initiator (Veranlasser, Auftraggeber, invites people)</li>
    <li>Moderator (Redner, taking the lead of the meeting, keeping things active)</li>
    <li>Keeper of Minutes (Schriftführer, meeting minutes – Mitschrift/Protokoll)</li>
    <li>Participant (Teilnehmer)</li>
</ul>

<p class="strong">INITIATOR</p>	
<ul class="m2">
    <li>Sets up the meeting</li>
    <li>Writes and distributes agenda</li>
    <li>Nominates Moderator and Keeper of Minutes</li>
</ul>

<p class="strong">MODERATOR</p>
<ul class="m2">
    <li>Leads thought the meeting</li>
    <li>Makes sure it stays in topic</li>
    <li>Get discussion back on track if it stays away from topic</li>
    <li>Solves communication issues between participants (it‘s important that he has no personal interests, he translate between all parts)</li>
    <li>(social skills)</li>
</ul>

<p class="strong">KEEPER OF MINUTES</p>	
<ul class="m2">
    <li>Takes note during meeting</li>
    <li>Documents decision and to-dos</li>
    <li>Sends out Meeting Minutes to participants after meeting</li>
</ul>

<p class="strong">MEETING RULES</p>	
<ul class="m2">
    <li>Every meeting needs an agenda(Tagesordnung)</li>
    <li>Results have to be documented</li>
    <li>Arrive on time</li>
    <li>End 5-10min early <br>
        <span class="fS-08">(Meeting Rooms should be free so that the next person can prepare their meeting. If you now the meeting needs 60 minutes, than booked the room for 10 more minutes.)</span>
    </li>
    <liPrepare yourself></li>
    <li>If agenda or reason for you being invited is not clear, ask up front</li>
</ul>

<p class="strong">SOME MORE MEETING RULES</p>
<ul class="m3">
    <li>Moderator should pass on his role if personally involved in a discussion topic</li>
    <li>When having a call, book longer than you think you will need – time seems to always be running out</li>
    <li>If you really think you do not belong in a meeting, tell the initiator</li>
    <li>Unbook room if you are not here</li>
    <li>Tell the organizer if you cannot make it to the meeting</li>
    <li>Revert meeting room to original configuration before leaving </li>
</ul>

