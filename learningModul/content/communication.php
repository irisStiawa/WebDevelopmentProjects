<h4 class="m1">Was ist Employer Branding?</h4>
<p class="m1">Arbeitgeber*innen wollen einer starken Arbeitgeber*innenmarke werden.</p>

<p class="strong">Websuche:</p>

<p class="m1">= ist eine unternehmensstrategische Maßnahme, bei der Konzepte aus dem Marketing – insbesondere der Markenbildung – angewandt werden, um ein Unternehmen insgesamt als attraktiven Arbeitgeber darzustellen und von anderen Wettbewerbern im Arbeitsmarkt positiv abzuheben (zu positionieren). Der Begriff taucht in der englischsprachigen Literatur erstmals 1996 im Fachartikel „The Employer Brand“ von Tim Ambler und Simon Barrow im Journal of Brand Management auf. Das Ergebnis von Employer Branding ist die Arbeitgebermarke (engl. Employer Brand), also die vom Unternehmen gezielt gestaltete Art und Weise, wie ein Unternehmen im Arbeitsmarkt als Arbeitgeber wahrgenommen wird. Insofern unterscheidet sich die Arbeitgebermarke vom Arbeitgeberimage, das eher historisch und primär durch Medieneinflüsse entstanden ist.</p>

<p class="m1">Das Ziel von Employer Branding besteht im Wesentlichen darin, aufgrund der erhofften Marketingwirkung die Effizienz der Personalrekrutierung als auch die Qualität der Bewerber dauerhaft zu steigern. Außerdem sollen qualifizierte und engagierte Mitarbeiter durch eine höhere Identifikation und durch den Aufbau einer emotionalen Bindung langfristig an das Unternehmen gebunden werden.</p>

<p class="m3">Beispiele für rein imageorientierte Kampagnen in Deutschland sind die „Be-Lufthansa“-Kampagne von Lufthansa, die „are you automotivated?“-Kampagne von Continental oder die „Passion Wanted!“-Kampagne von McKinsey & Company Deutschland. Häufig zitierte und als Beispiele für Unternehmen verwendet, die in Deutschland erfolgreiche Employer-Branding-Prozesse aufgesetzt haben, sind Orthomol („Überzeugendes unternehmen“), MARS („Freedom takes courage. We take the courageous“), Capgemini sd&m („Substanz? Herzlich? Willkommen!“) sowie seit Anfang 2010 auch die Techniker Krankenkasse („Sind Sie das?“).</p>


