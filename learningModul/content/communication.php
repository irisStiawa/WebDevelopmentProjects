	<h4>Stress </h4>
    <p class="fS-08 m1">= warnt vor Gefahren (Fluchtreaktion)</p>
	
    <p class="string">Was ist Stress?</p>
<ul class="m3">
    <li>der Begriff bedeutet auf Englisch Druck oder Anspannung</li>
    <li>die Verbiegung eines Materials unter Druckbelastung wird in der Physik als Stress bezeichnet</li>
    <li>in den 1930iger Jahren hat der Chemiker Hans Selye die Grundlagen der Lehre vom Stress und vom allgemeinen Adaptationssyndrom oder Selye-Syndrom entwickelt</li>
</ul>

<p class="strong text-center">2 Arten von Stress</p>
<p class=" text-center m2">Eu-Stress <br>Dis-Stress</p>


<div class="flex-top m3">
    <div>
        <p class="strong">Eu-Stress</p>
        <ul>
            <li>positiver Stress</li>
            <li>Anreiz – Motivation</li>
