<p class="strong">Sprint und Sprint Backlog</p>
<ul class="m3">
    <li><span class="strong">Sprint</span> = Arbeitszyklus/Projektphase</li>
    <li>Feste Zeitdauer(Time Box)</li>
    <li>Inhalte des Sprints kommen aus Product Backlog</li>
    <li>Übertrag in den <span class="strong">Sprint Backlog</span></li>
    <li>Jedes Team Member übernimmt Aufgaben eigenverantwortlich</li>
</ul>

<p class="strong">Sprint Review</p>
<ul class="m3">
    <li>Meeting mit allen Beteiligten</li>
    <li>Abschluss eines Sprints</li>
    <li>Vorstellung der Fortschritte (Product Increment)</li>
    <li>Abnahme durch Product Owner/Kunden/Anwender</li>
</ul>

<p class="strong">Sprint Retrospective</p>
<ul class="m3">
    <li>Meeting mit allen Beteiligten</li>
    <li>Überprüfen der Zusammenarbeit</li>
    <li>Interne Verbesserungen erarbeiten <br><br><span class="kursiv">Mögliche Anlässe</span><br>
    <ul>
        <li>Konflikte innerhalb des Teams</li>
        <li>Konflikte zwischen Team und Product Owner</li>
        <li>Projekt steckt fest</li>
    </ul></li>
</ul>

<p class="strong">Daily Scrum</p>	
<ul class="m1">
    <li>Tägliches Meeting des Project Teams</li>
    <li>ca. 15min(time boxed)</li>
    <li>Was wurde seit dem letzten Scrum gemacht?</li>
    <li>Was ist bis zum nächsten Scrum zu tun?</li>
    <li>Was behindert die Arbeit?</li>
</ul>

