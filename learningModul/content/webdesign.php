            <td>lässt den Abstand zwischen den Wörtern variieren(nicht Ideal zum lesen)</td>
        </tr>
        <tr>
            <td class="strong">Flattersatz:</td>
            <td>rechtsbündiger Absatz</td>
        </tr>
        <tr>
            <td class="strong">Kapitälchen:</td>
            <td>stilistisch altertümlich</td>
        </tr>
        <tr>
