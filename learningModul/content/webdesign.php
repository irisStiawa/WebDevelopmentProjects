<h4>Typographie</h4>
<p class="fS-08 m1">= Frage der Typographie, serifen Schrift können bei niedriger Bildschirmauflösung verschwimmen, daher serifenlose Schrift geeigneter in der Webanwendung</p>

<p class="strong m1">Serifen Schrift, erste anerkannte Schrift:</p>
<h4 class="timesRoman m1 text-center">Times New Roman</h4>

<p class="strong m1">Serifenlose Schrift, seit Mitte bis Ende des 19 Jahrhunderts (wird auch groteske Schrift bezeichnet):</p>
<h4 class="arial m1 text-center">Arial</h4>

<p class="strong m1">Mono-Space Schriften, aufgrund der Schreibmaschine/benötigen immer denselben Platz:</p>
<h4 class="arial m3 text-center">Courier New</h4>


<h4 class="m1">Eckpunkte</h4>

<table class="m3">
    <tbody>
        <tr>
            <td class="strong">Initalen:</td>
