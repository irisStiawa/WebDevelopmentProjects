<h4 class="m1">Grundregeln</h4>
<ul class="m3">
    <li>Zeilenabstand</li>
    <li>Welche Art von Text</li>
    <li>Zeilenlänge: 45-75 Zeichen</li>
    <li>Flattersatz > Blocksatz</li>
    <li>Schriftgröße</li>
    <li>Kontrast um Übersicht zu ermöglichen</li>
    <li>Zeichenabstände(engl. kerning) nur bei Blockbuchstaben</li>
    <li>Schriftarten (serif, sans-serif) – auszuwählen nach dem Aspekt der Lesbarkeit (niemals zwei Schriftarten der selben Klasse – serifen mit serifen) <span class="strong">ERLAUBT:</span> serifenlose mit serifen</li>
</ul>
		

