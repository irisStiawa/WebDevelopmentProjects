<h4 class ="m1">Gestaltungsfehler</h4>

<ul class="m3">
    <li>selbe Menüschrift wie Logo</li>
    <li>Logo gleiche Größe wie Menü</li>
    <li>Farben die das Logo verschlingen</li>
    <li>Menü das dem Prinzip der Nähe wiederspricht (Buttons zu weit auseinander)</li>
    <li>alle Bereiche auf Zwang definieren (alle Bereiche mit Hintergrund definieren)</li>
    <li>andere Formate sollten sich deutlich unterscheiden und nicht verschwimmen
</li>
</ul>

