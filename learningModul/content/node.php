<h4>Server</h4>
<p class="kursiv fS-08 m1">(kann jeder beliebige PC sein, hat keine besondere Hardware, der eine Server-Software hat)</p>

<ul class="m3">
    <li>wartet auf ein Request(Anfrage)</li>
    <li>sendet Response</li>
</ul>

<p class="strong">Requests</p>
<p class="kursiv fS-08 m1">(der Domäinname - Server übersetzt die URL in eine IP Adresse und an diese IP Adresse wird die Anfrage weitergleitet)
</p>

<ul class="m2">
    <li>GET (read)</li>
    <li>POST (create)</li>
    <li>PUT (update)</li>
    <li>DELETE (delete)</li>
</ul>

<p class="text-center m2 border-black kursiv"><span class="strong">Routing:</span> Art wie der Server auf Request reagiert <br> <span class="strong">Port:</span> Zugang(Eingang) zum Server – dieser wird mit einem Doppelpunkt hinter der IP aufgerufen</p>

<p class="strong">hilfreiche Software:</p>
<ul class="m3">
    <li>express</li>
    <li>dotenv</li>
    <li>RESTer (Postrequests ohne Formulare für Entwicklungsumgebung)</li>
</ul>

